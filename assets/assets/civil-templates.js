function loadTemplate() {
    const selector = document.getElementById('templateSelector');
    const templateKey = selector.value;
    
    if (civilTemplates[templateKey]) {
        const t = civilTemplates[templateKey];
        document.getElementById('petitionSubject').value = t.subject;
        document.getElementById('petitionBody').value = t.body;
        document.getElementById('petitionRequests').value = t.requests;
        document.getElementById('attachedDocs').value = t.docs;
        document.getElementById('witnessesNames').value = t.witnesses;
        generatePetition();
    }
}
